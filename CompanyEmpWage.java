
public class CompanyEmpWage {
	
	public int wage_per_hour; //Stores wage per hour
	public int max_working_days; //stores maximum workings days
	public int max_working_hours; //Stores maximum working hours
	String company=""; //Stores Company name
	
	public CompanyEmpWage(int wage_per_hour, int max_working_days, int max_working_hours, String company) {
		super();
		this.wage_per_hour = wage_per_hour;
		this.max_working_days = max_working_days;
		this.max_working_hours = max_working_hours;
		this.company = company;
	}
	
	

}
